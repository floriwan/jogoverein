      <section class="box feature">
          <p>The program of <strong>JoGoVEREIN</strong> is characterised by a high degree of flexibility.
          While it is geared towards the club management, but offers plenty of freedom for their own
          Customizations and extensions. The default settings of the programme which can be largely
          fit individual needs.
          </p>
          <ul>
              <li>Either Microsoft Access or MySQL database format</li>
              <li>Data can copy from access to MySQL and vice versa.</li>
              <li>Import of data already acquired member data</li>
              <li>Data import and export of bookings in the SWIFT format</li>
              <li>Free definition of the data fields of the Member record</li>
              <li>Calculation of data field contents with arithmetic and logical expressions</li>
              <li>Edit the database content using Microsoft Access possible</li>
              <li>Free input of SQL queries that are possible in Microsoft Access</li>
              <li>Integration of the database fields in Microsoft Word and Outlook</li>
              <li>Data export to Microsoft Excel, Word, access, and text format</li>
              <li>Data export in the text encodings: DOS, ASCII, ANSI, or UNICODE possible.</li>
              <li>Reverse the printed output to a text file (without printer control characters)</li>
              <li>User interface and help file can be switched between german/English</li>
          </ul>
      </section>

