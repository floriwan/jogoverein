                              <p>Das Programm <strong>JoGoVEREIN</strong> zeichnet sich durch eine hohe Flexibilit&auml;t aus.
                              Es ist zwar auf die Vereinsverwaltung ausgerichtet, bietet aber viel Freiheit f&uuml;r eigene
                              Anpassungen und Erweiterungen. Die Voreinstellungen des Programms lassen sich sehr weitgehend den
                              individuellen W&uuml;nschen anpassen.
                              </p>
                              <ul>
                                  <li>Datenbankformat wahlweise Microsoft Access oder MySQL</li>
                                  <li>Die Daten lassen sich von Access nach MySQL und umgekehrt kopieren.</li>
                                  <li>Datenimport bereits erfasster Mitgliederdaten</li>
                                  <li>Datenimport und -export von Buchungen im SWIFT-Format</li>
                                  <li>Freie Definition der Datenfelder des Mitgliederdatensatzes</li>
                                  <li>Berechnung von Datenfeldinhalten mit arithmetischen und logischen Ausdr&uuml;cken</li>
                                  <li>Bearbeitung der Datenbankinhalte mit Microsoft-Access m&ouml;glich</li>
                                  <li>Freie Eingabe der in Microsoft Access m&ouml;glichen SQL-Abfragen</li>
                                  <li>Einbindung der Datenbankfelder in Microsoft-Word und Outlook</li>
                                  <li>Datenexport nach Microsoft-Excel, -Word, -Access und Textformat</li>
                                  <li>Datenexport in den Textkodierungen: DOS, ASCII, ANSI oder UNICODE m&ouml;glich.</li>
                                  <li>Umsteuerung der Druckausgabe in eine Textdatei (ohne Druckersteuerzeichen)</li>
                                  <li>Benutzeroberfl&auml;che und Hilfedatei lassen sich umschalten zwischen deutsch/englisch</li>
                              </ul>
                          </section>

