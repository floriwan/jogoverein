                <p>Das Programm lässt sich nicht mehr auf andere Sprachen umschalten. Ein Fehler bei der Verwaltung von Zählern (Gas/Wasser) ist behoben. Das Fehlverhalten beim Import von Mitgliederdaten im Textformat bezüglich der Mitgliedsnummer und der ersten Zeile mit den Feldnamen ist behoben. Aktionsabfragen werden wieder korrekt angezeigt. Das Datenfeld MNr lässt sich wieder editieren.</p>
