                <h3>Version 3.4.6</h3>
                <p>Das Thema "Serienbriefe" wurde vollständig überarbeitet. Es wurde das Programm optimiert, die Dokumentation ergänzt und mit der Programminstallation gibt es im Ordner {eigene Dokumente}\JoGoVEREIN zusätzliche Beispiel-Serienbriefe. Auf der Homepage ist ein Video zu diesem Thema aufrufbar.</p>
            </li>
