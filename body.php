                <h3>Version 3.4.5</h3>
                <p>Die mit Version 3.4.4 eingeführte Steuerdatei zum Versand von Serienbriefen bietet neue Möglichkeiten. Die in vielen Fenstern zu findenden Symbole zur Auswahl von Datensätzen (alle, aktuelle, vorige, künftige, Auswahl, Favorit) sind zusammengefasst und vereinheitlicht. Der Import von Daten hat einige zusätzliche Möglichkeiten.</p>
