        <h2>JoGoVEREIN</h2>
        <p>Vereinsverwaltung mit hoher Flexibiltät</p>
        <p><a href="http://deupad.org/factsheet.asp?http://jogoverein.goeldenitz.org/pad_file.xml" target="_blank">
           <i class="icon fa-external-link"></i> DeuPAD Factsheet</a></p>
   </section>

      <section id="main" class="container">
      <section class="box feature">
          <p>Das Programm <strong>JoGoVEREIN</strong> ist primär für Vereinsaufgaben geeignet. Es verwaltet die Mitglieder mit deren Beitragskonten und führt das Kassenbuch. Die Programmarchitektur ist sehr flexibel und bietet viel Freiheit für eigene Anpassungen und Erweiterungen. Die Voreinstellungen des Programms lassen sich sehr weitgehend den individuellen Wünschen anpassen.
          </p>
          <ul>
              <li>Die Daten werden auf dem lokalen Rechner im Format <b>Microsoft Access</b> gespeichert.</li>
              <li>Eine Speicherung der Daten auf einem Server mit Zugriff über <b>MySQL</b> ist ebenfalls möglich</li>
              <li>Die Daten lassen sich vom Microsoft Access nach MySQL und umgekehrt kopieren.</li>
              <li>JoGoVEREIN verwaltet auch übergeordnete <b>Vereinsstrukturen</b>, wie z.B. Bezirke oder Abteilungen.</li>
              <li>Zwei Datenbanken lassen sich gleichzeitig laden und die Abweichungen anzeigen.</li>
              <li>Auf der <b>Eingabemaske</b> lassen sich die Datenfelder frei anordnen und farblich hervorheben.</li>
              <li>Den Mitgliedern lassen sich <b>Bilder</b> (z.B. Portraits) zuordnen.</li>
              <li>Alle Änderungen am Mitgliederbestand werden in einem <b>Logbuch</b> notiert.</li>
              <li>Die Ausdrucke von <b>Listen und Etiketten</b> lassen sich vom Anwender frei gestalten.</li>
              <li>Zur Verwaltung der Mitgliedsbeiträge sind jedem Mitglied ein oder mehrere Konten zugeordnet.</li>
              <li>Die Höhe des Mitgliedsbeitrags ist aus Feldinhalten des Mitgliederdatensatzen berechenbar.</li>
              <li>Die Ein- und Ausgaben lassen sich mit einem <b>Haushaltsplan</b> übersichtlich darstellen.</li>
              <li>Beiträge lassen sich mit dem <b>SEPA-Lastschriftverfahren</b> einziehen.</li>
              <li>Der Zugriff auf die Datenbankdatei lässt sich über ein <b>Passwort</b> schützen.</li>
              <li>Zusätzlich lassen sich die Daten verschlüsselt abspeichern.</li>
              <li>Es sind die Bankleitzahlen, BIC, Postleitzahlen und Ortsnetzkennzahlen abfragbar.</li>
              <li>Die o.g. Kennzahlen gibt es auch für die <b>Schweiz</b> (außer ONKZ) und <b>Österreich</b>.</li>
              <li>Aus Bankleitzahl und Kontonummer lassen sich die <b>IBAN</b> und der Bankname berechnen.</li>
              <li>Aus IBAN lässt sich die BIC und der Bankname berechnen.</li>
              <li>Nach Eingabe der Postleitzahl wird der Ortsname automatisch ausgefüllt.</li>
              <li>Daten für <b>Zuwendungsbestätigungen</b> werden bereitgestellt.</li>
              <li>Mit Memo-Feldern lassen sich beliebig umfangreiche Texte einzelnen Mitgliedern zuordnen.</li>
              <li>Übersichtsinformationen lassen sich auch als <b>Grafik</b> Torten-/Balkendiagramm) ausgeben.</li>
              <li>Abrechnung von <b>Zählerständen</b> für einen <b>Kleingartenverein</b> mit Exportfunktionen.</li>
              <li>Programm und Hilfedatei lassen sich auf die Sprachen deutsch, englisch, französisch und russisch</b> umschalten.
              <li><b>Datenimport</b> bereits erfasster Mitgliederdaten in den Formaten Access, Excel und Text ist möglich.</li>
              <li>Datenimport und -export von Buchungen im SWIFT-Format</li>
              <li>Mitglieder- und Kassendaten lassen sich in den Formaten Microsoft-Excel, -Access und Text exportieren</li>
              <li>Die Datenfelder des Mitgliederdatensatzes lassen sich nach Name, Datentyp und Länge frei definieren.</li>
              <li>Datenfeldinhalten können mit Formeln aus anderen Datenfelder berechnet werden.</li>
              <li>Alle mit Microsoft Access zulässigen SQL-Abfragen sind von JoGoVEREIN ausführbar.</li>
              <li>Die Datenfelder können direkt in Microsoft-Word oder Outlook für Serienbriefe eingebunden werden.</li>
              <li>Datenexport nach Microsoft-Excel, -Word, -Access und Textformat</li>
              <li>Datenexport ist in den Textkodierungen: DOS, ASCII, ANSI oder UNICODE möglich.</li>
              <li>Druckausgaben lassen sich als Textdatei (ohne Druckersteuerzeichen) ausgeben.</li>
          </ul>
      </section>

