                <p>Es lassen sich Daten zu <b>Mitgliedergruppen</b> speichern, die ähnlich wie die Mitgliederdaten zu bearbeiten sind. Das Besondere
                der Gruppendaten ist die Verknüpfung von ein oder mehreren Mitgliedern mit einem Gruppendatensatz. Mit dieser Methode lassen sich
                Vereinsstrukturen wie z.B. Bezirke oder Abteilungen erfassen. Zum Datenaustausch steht ein Gerüst von Funktionen bereit..<br></br>
                Um beliebige Informationen abrufbar zu haben, können <b>externe Tabellen</b> in JoGoVEREIN integriert werden.
