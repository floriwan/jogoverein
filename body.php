                <p><b>Mitgliederdatensätze</b> lassen sich neu <b>nummerieren</b>. Dazu muss in dem Fenster "Mitgliedertabelle" >> "Spalteninhalte neu zuweisen" die Tabellenspalte "MNr" gewählt werden.</p>
            </li>
            <li>
