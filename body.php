                <h3>Version 3.4.3</h3>
                <p>Im Kassenbuch und beim Buchen werden <b>Gruppen</b> genauso behandelt wie bisher die Mitglieder. Dafür hat das Kassenbuch eine zusätzliche Spalte "Gruppe". Buchungen lassen sich einer Gruppe zuordnen.</p>
