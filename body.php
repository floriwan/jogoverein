                <span class="date">Mrz<strong>16</strong></span>
                <h3>Version 3.4.4</h3>
                <p>Zum Versand von Serienbriefen lässt sich eine  Textdatei anlegen mit allen für den <b>Serienbrief</b> benötigten Daten. Das besondere dabei ist die Möglichkeit, Beziehungen zu anderen Datensätzen aufzulösen. Wenn z.B. zu den Mitgliedern auch Mitgliedergruppen verwaltet werden, können im Serienbrief Datenfelder aus beiden Tabellen angesprochen werden. Auch Verweise von einem Mitglied zu einem anderen Mitglied (z.B. dem Beitragszahler) lassen sich auflösen.</p>
            </li>
