               <h3>Version 3.4.2</h3>
               <p>Es gibt eine automatische <b>Datensicherung</b> mit einer vorgebbaren Anzahl an Versionen.</p>
            </li>
            <li>
               <span class="date">Dez<strong>27</strong></span>
               <h3>Version 3.4.1</h3>
               <p>Änderungen am Gruppendatensatz werden in einem eigenen Logbuch gespeichert. 
               Beim E-Mail Versand Fehler bei Übertragung der Betreffzeile und bei eingebetteten Bildern im HTML-Text behoben.
               In der Eingabemaske des Mitgliederdatensatz zeigen Datenfelder die mit 'MNr' beginnen, beim Editieren eine Auswahltabelle aller Mitgliedsnamen an.
               Analoges gilt für den Gruppendatensatz und 'GNr'.</p>
