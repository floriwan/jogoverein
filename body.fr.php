          <p>Le programme de <strong>JoGoVEREIN</strong> est caractérisé par un degré élevé de flexibilité.
          Alors qu’il est orienté vers la direction du club, mais offre beaucoup de liberté pour leur propre
          Les personnalisations et les extensions. Les paramètres par défaut du programme qui peut être en grande partie
          répondre à des besoins individuels.
          </p>
          <ul>
              <li>Soit Microsoft Access ou le format de base de données MySQL</li>
              <li>Peuvent copier des données d’accès à MySQL et vice versa.</li>
              <li>Importation des données déjà acquises les données membres</li>
              <li>Données, importation et exportation de réservations au format SWIFT</li>
              <li>Définition libre des champs de données de l’enregistrement membre</li>
              <li>Calcul des données de champ contenu avec des expressions arithmétiques et logiques</li>
              <li>Modifier le contenu de la base de données à l’aide de Microsoft Access possible</li>
              <li>Entrée libre de requêtes SQL qui sont possibles dans Microsoft Access</li>
              <li>Intégration des champs de base de données dans Microsoft Word et Outlook</li>
              <li>Exporter des données vers Microsoft Excel, Word, access et format texte</li>
              <li>Data export dans les encodages de texte : DOS, ASCII, ANSI ou UNICODE possible.</li>
              <li>Inverser la sortie imprimée vers un fichier texte (sans les caractères de contrôle d’imprimante)</li>
              <li>User interface et fichier d’aide peut être commuté entre allemand/anglais</li>
          </ul>
